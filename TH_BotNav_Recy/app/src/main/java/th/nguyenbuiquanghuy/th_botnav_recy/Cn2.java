package th.nguyenbuiquanghuy.th_botnav_recy;

public class Cn2 {
    String image;
    String text;

    public Cn2(String image, String text) {
        this.image = image;
        this.text = text;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
